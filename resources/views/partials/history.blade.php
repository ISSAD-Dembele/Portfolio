                <strong>l'obtention de mon baccalauréat scientifique en août 2021</strong>.
                Je me sentais fier, mais inquiet pour la suite.
            </p>
            <p>
                Puis vint mon arrivée au Maroc en 2023. Ces trois années ont bouleversé mon existence
                une saga si dense que je pourrais y consacrer des jours entiers. Grâce à ces expériences,
                j'ai gravé en moi deux leçons fondamentales :
                <strong>« Qui était là quand tu étais dans le besoin ? »</strong> et
                <strong>« Qui t'a aidé à t'en sortir ? »</strong>
            </p>
