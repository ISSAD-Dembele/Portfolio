            <h2>Les Premiers Pas</h2>
            <p>
                Je suis né en septembre 2004 dans la famille Dembélé, aîné des garçons au sein d'un
                patrimoine familial riche de traditions. Je m'appelle <strong>ISSA D.</strong>,
                originaire du Mali, le pays de la prospérité et de la fidélité. Tel est le fil
                conducteur de mon histoire, mêlant épreuves, passions et espoirs.
            </p>
            <p>
                J'ai grandi entouré de ma famille Al hamdulillah, qu'Allah m'ait béni de ce bonheur
                simple. Ma mère me raconte souvent que j'étais un enfant potelé, si gros que j'avais du
                mal à marcher. Avec un sourire complice, elle ajoute que j'étais le seul parmi ses
                enfants à avoir un jour décidé d'arrêter de téter son lait maternel — j'avais découvert
                mon péché mignon : la nourriture.
            </p>
        </div>

        {{-- Chapter 2 --}}
        <div class="histoire-chapter">
            <h2>Mes Débuts Scolaires et Ma Première Passion</h2>
            <p>
                Lors d'un TEDx, j'avais défendu un thème qui résonne encore aujourd'hui :
                <strong>« Le talent m'a ignoré, mais j'ai choisi l'effort »</strong>. Je n'avais pas menti.
            </p>
            <p>
                Le dessin me passionnait depuis l'enfance. Au début, rien n'était facile. Mes parents
                désapprouvaient cette vocation, et mes croquis incessants se soldaient par de mauvaises
                notes à la maison. Ils me sermonnaient, mais cette flamme restait vive, inébranlable.
                En grandissant, j'ai dû affronter la réalité d'un monde complexe et impitoyable.
                Avec le temps, j'ai rangé mes crayons pour embrasser une nouvelle passion : le football.
            </p>
        </div>

        {{-- Chapter 3 --}}
        <div class="histoire-chapter">
            <h2>Le Football, la Blessure et le Renoncement</h2>
            <p>
                Jusqu'à mes années de lycée, je croyais que c'était la bonne voie. Mais un jour funeste,
                <strong>une blessure grave au genou a tout changé</strong>. Elle m'a freiné à jamais.
                Durant cette année de calvaire, le joueur en moi s'éteignait peu à peu. Voir mon jeune
                frère sur le terrain, tentant d'imiter mes gestes, me déchirait le cœur.
            </p>
            <p>
                Quand les médecins m'ont autorisé à reprendre, le doute m'habitait. J'ai essayé, puis
                abandonné. Le feu du joueur était presque mort. J'ai décidé de lâcher prise. Le foot ne
                serait plus qu'un divertissement occasionnel. Je me suis promis :
                <strong>« J'emprunterai un autre chemin. Être joueur n'est pas ma destinée. »</strong>
            </p>
        </div>

